/*****************************************************************************
** Description :  set specify default CAPKs to EMV-kernel
** Parameters  :  input:
                  (1)E_CARD_TYPE eCardType: which card type CAPK you need to set
                  (2)bool bIsLiveCapk: whether it is live CAPK
                  
** Return      :  (1)SDK_PARA_ERR: input parameter error
                  (2)SDK_ERR: set CAPKs fail
                  (3)SDK_OK: set CAPKs success
                  
** Author      :  wenmin   2016-10-18 
** Remarks     :  
*****************************************************************************/
s32 EmvSetCapk(E_CARD_TYPE eCardType, bool bIsLiveCapk)
{
    s32 i = 0, num = 0;
    s32 ret = SDK_OK;
    const SDK_ICC_CAPK_STRUCT *pst_capk = NULL;
    u8 card_type[64] = {0};
    u8 tmp[128] = {0};

    if (eCardType < VISA || eCardType > CUP)
    {
        Assert(0);
        return SDK_PARA_ERR;
    }

    switch (eCardType)
    {
         case VISA:
            strcpy(card_type, STR_CARD_VISA);
            num = (bIsLiveCapk) ? (sizeof(TabVisaLiveCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT))
                                : (sizeof(TabVisaTestCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT));
            pst_capk = (bIsLiveCapk) ? TabVisaLiveCAPKs : TabVisaTestCAPKs;
            break;

         case MASTER:
            strcpy(card_type, STR_CARD_MC);
            num = (bIsLiveCapk) ? (sizeof(TabMasterLiveCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT))
                                : (sizeof(TabMasterTestCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT));
            pst_capk = (bIsLiveCapk) ? TabMasterLiveCAPKs : TabMasterTestCAPKs;
            break;

         case JCB:
            strcpy(card_type, STR_CARD_JCB);
            num = (bIsLiveCapk) ? (sizeof(TabJcbLiveCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT))
                                : (sizeof(TabJcbTestCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT));
            pst_capk = (bIsLiveCapk) ? TabJcbLiveCAPKs : TabJcbTestCAPKs;
            break;

         case AMEX:
            strcpy(card_type, STR_CARD_AMEX);
            num = (bIsLiveCapk) ? (sizeof(TabAmexLiveCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT))
                                : (sizeof(TabAmexTestCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT));
            pst_capk = (bIsLiveCapk) ? TabAmexLiveCAPKs : TabAmexTestCAPKs;
            break;

         case CUP:
            strcpy(card_type, STR_CARD_CUP);
            num = (bIsLiveCapk) ? (sizeof(TabCupLiveCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT))
                                : (sizeof(TabCupTestCAPKs) / sizeof(SDK_ICC_CAPK_STRUCT));
            pst_capk = (bIsLiveCapk) ? TabCupLiveCAPKs : TabCupTestCAPKs;
            break;

         default:
            break;
    }

    if(SDK_OK != sdkIccAddAnyCAPKList(pst_capk, num))
    {
        for (i = 0; i < num; i++)
        {
            ret = sdkIccAddAnyCAPKList(&pst_capk[i], 1);
            Trace("emv", "sdkIccAddAnyCAPKList: ret = %d\r\n", ret);

            if (SDK_OK != ret)
            {
                sprintf(tmp, "%s: %s[%02X]", card_type, STR_INFO_CAPKI, pst_capk[i].CAPKI);
                sdkDispClearScreen();
                sdkDispFillRowRam(SDK_DISP_LINE2, 0, STR_INFO_ADD_CAPK_FAIL, SDK_DISP_DEFAULT);
                sdkDispFillRowRam(SDK_DISP_LINE3, 0, tmp, SDK_DISP_DEFAULT);
                sdkDispBrushScreen();
                sdkKbWaitKey(SDK_KEY_MASK_ENTER | SDK_KEY_MASK_ESC, 0);
                return ret;
            }
        }
    }
    else
    {
        sdkDbCreateTable(&TabCapk);
        for (i = 0; i < num; i++)
        {
            memcpy(&_gstCapk, &pst_capk[i], sizeof(SDK_ICC_CAPK_STRUCT));
            sdkDbInsertRecord(&TabCapk);
        }
    }
    
    return SDK_OK;
}

/*****************************************************************************
** Description :  config all default CAPKs to EMV-kernel
** Parameters  :  input
                  (1)bool bIsLiveCapk: whether it is live CAPK
                  
** Return      :  None
** Author      :  wenmin   2016-10-18 
** Remarks     :  
*****************************************************************************/
void EmvSetAllDefaultCapk(bool bIsLiveCapk)
{
    sdkIccDelAllCAPKLists();
    EmvSetCapk(VISA, bIsLiveCapk);
    EmvSetCapk(MASTER, bIsLiveCapk);
    EmvSetCapk(JCB, bIsLiveCapk);
    EmvSetCapk(AMEX, bIsLiveCapk);
    EmvSetCapk(CUP, bIsLiveCapk);
    sdkIccSaveCAPKFile();
}

/*****************************************************************************
** Description :  config CAPKs to EMV-kernel and database
** Parameters  :  input
                  (1)bool bIsLiveCapk: whether it is live CAPK
                  
** Return      :  (1)SDK_ERR: set CAPKs fail
                  (2)SDK_OK: set CAPKs success
                  
** Author      :  wenmin   2016-10-18 
** Remarks     :  If there is not CAPKs in the database, it will set default
                  CAPKs to the EMV-kernel, and set default CAPKs to the database.
                  If there have CAPKs in the database, it will use the CAPKs in
                  database to set to the EMV-kernel.
*****************************************************************************/
s32 EmvInitCapk(bool bIsLiveCapk)
{
    s32 num = 0;
    s32 i = 0;
    SDK_ICC_CAPK_STRUCT *pst_capk = NULL;
    
    if(DB_EXIT_SUCCESS != sdkDbOpen())
    {
        EmvSetAllDefaultCapk(bIsLiveCapk);
        return SDK_ERR;
    }
    
    num = sdkDbGetRecordTotalNum(TabCapk.pasTabName, NULL, 0);
    if(num < 0)
    {
        EmvSetAllDefaultCapk(bIsLiveCapk);
        return SDK_ERR;
    }
    
    pst_capk = (SDK_ICC_CAPK_STRUCT *)sdkGetMem(sizeof(SDK_ICC_CAPK_STRUCT) * num);
    if(NULL == pst_capk)
    {
        return SDK_ERR;
    }

    for(i = 0; i < num; i++)
    {
        memset(&_gstCapk, 0, sizeof(SDK_ICC_CAPK_STRUCT));
        if(DB_EXIT_SUCCESS != sdkDbGetRecordByIndex(TabCapk.pasTabName, i, (void *)&TabCapk))
        {
            sdkFreeMem(pst_capk);
            return SDK_ERR;
        }
        memcpy(&pst_capk[i], &_gstCapk, sizeof(SDK_ICC_CAPK_STRUCT));
    }
    sdkIccAddAnyCAPKList(pst_capk, num);
    sdkIccSaveCAPKFile();
    sdkFreeMem(pst_capk);
    
    return SDK_OK;
}

